void main() {
  /**
   * +
   * -
   * *
   * /   = pembagian hasil double
   * ~/ = pembagian hasil int
   * %  = sisa bagi
   */

  var first = 10;
  var second = 3;

  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);
}
